#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 1;
	while (s[i] != '\0')
	{
		i++;
	}
	while (i >= 0)
	{
		if (s[i] == c)
		{
			return ((char *)&s[i]);
		}
		else
		{
			i--;
		}
	}
	return (NULL);
}
